"""
Entrar com um número e imprimir uma das mensagens: maior do que 20, igual a
20 ou menor do que 20.

"""
valor = float(input(print("Entre com o valor ")))
if valor > 20:
    print("Maior que 20")
elif valor < 20:
    print("O valor é menor que 20")
else:
    print("O valor é igual a 20")
    