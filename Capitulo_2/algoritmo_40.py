"""

Entrar com dois numeros inteiros e imprimir a seguinte saída:
dividendo
divisor:
quociente:
restos

"""

n1 = int(input(print("Entre com o VALOR INICIAL:\n")))
n2 = int(input(print("Entre com o VALOR FINAL:\n")))

dividendo = n1
divisor = n2
quociente = n1/n2
resto = n1%n2

print("Dividendo é: \a",dividendo), print("\a")
print("Divisor é: \a",divisor), print("\a")
print("Quociente é: \a",quociente), print("\a")
print("Resto é: \a",resto), print("\a")

